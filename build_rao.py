#!/usr/bin/env python3
"""Build the bundled current-edition RAO content pack from the PCA source.

The Rules of Assembly Operations are deliberately a *supplementary* pack: they
are included for study, but are not represented as part of the PCA Constitution.
The official source labels this edition "with revisions adopted through the 52nd
General Assembly (2025)."
"""

from __future__ import annotations

import html
import json
import re
import subprocess
from pathlib import Path

from lxml import html as lxml_html

SOURCE = "https://www.pcaac.org/book-of-church-order/rules-of-assembly-operations/"
HERE = Path(__file__).resolve().parent
OUT = HERE / "content" / "rao.js"


def fetch() -> str:
    result = subprocess.run(
        ["curl", "-fsSL", "-A", "pca-constitution-reader/1.0", SOURCE],
        check=True,
        capture_output=True,
    )
    return result.stdout.decode("utf-8")


def text(element) -> str:
    return re.sub(r"\s+", " ", element.text_content()).strip()


def block(element) -> list[str]:
    value = text(element)
    indent = 0
    style = element.get("style", "")
    match = re.search(r"padding-left:\s*(\d+)px", style)
    if match:
        indent = min(4, max(0, int(match.group(1)) // 40))
    marker = re.match(r"(?P<marker>(?:[a-z]\.|\(?\d+\)|\d+\.))\s+(?P<body>.+)", value, re.I)
    if marker and indent:
        return ["i", indent, marker.group("marker"), marker.group("body")]
    return ["p", indent, value]


def build(source: str) -> dict:
    root = lxml_html.fromstring(source)
    chapters: dict[str, dict] = {}
    order: list[str] = []

    for container in root.xpath('//*[starts-with(@id, "RAO_")]'):
        article = container.get("id", "").removeprefix("RAO_")
        if not article.isdigit():
            continue
        heading = container.xpath('.//h4[1]')
        title = text(heading[0]) if heading else f"Article {article}"
        sections: list[dict] = []
        current: dict | None = None
        prose: list[list[str]] = []
        for paragraph in container.xpath('.//p'):
            value = text(paragraph)
            if not value:
                continue
            match = re.match(rf"{article}-(\d+)[.]?\s*(.*)", value)
            if match:
                current = {"ref": f"{article}-{int(match.group(1))}", "blocks": [["p", 0, match.group(2)]]}
                sections.append(current)
                continue
            item = block(paragraph)
            if current is None:
                prose.append(item)
            else:
                current["blocks"].append(item)
        # Articles themselves are sometimes cited (e.g. "RAO 20").  They remain
        # addressable without duplicating every numbered provision in the reading view.
        article_intro = prose or [["p", 0, f"Article {article}: {title}. See the numbered provisions below."]]
        sections.insert(0, {"ref": article, "blocks": article_intro, "prose": True})
        chapters[article] = {"title": title, "sections": sections}
        order.append(article)

    assert order == [str(n) for n in range(1, 21)], f"expected RAO articles 1–20, got {order}"
    assert any(s["ref"] == "16-3" for s in chapters["16"]["sections"]), "RAO 16-3 missing"
    return {
        "format": "pca-constitution-pack",
        "version": 1,
        "kind": "book",
        "label": "Rules of Assembly Operations (RAO)",
        "notice": "Supplementary PCA material · current text through the 52nd General Assembly (2025) · not part of the PCA Constitution.",
        "component": {
            "key": "rao",
            "abbr": "RAO",
            "name": "Rules of Assembly Operations",
            "division": "supplemental",
            "icon": "scroll",
            "nonConstitutional": True,
            "tag": "Rules for the ecclesiastical operation of the General Assembly — not part of the PCA Constitution.",
            "lede": "The current Rules of Assembly Operations, governing the ecclesiastical work of the General Assembly.",
            "meta": "20 articles · current through the 52nd General Assembly (2025) · supplementary PCA material",
        },
        "order": order,
        "chapters": chapters,
    }


def main() -> None:
    pack = build(fetch())
    OUT.write_text(
        "/* Generated by build_rao.py from the PCA's official RAO page. */\n"
        "window.BUNDLED_PACKS = window.BUNDLED_PACKS || [];\n"
        f"window.BUNDLED_PACKS.push({json.dumps(pack, ensure_ascii=False, separators=(',', ':'))});\n",
        encoding="utf-8",
    )
    print(f"wrote {OUT}: {sum(len(c['sections']) for c in pack['chapters'].values())} citeable entries")


if __name__ == "__main__":
    main()
